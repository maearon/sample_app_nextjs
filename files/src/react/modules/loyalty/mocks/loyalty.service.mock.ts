import {rest} from 'msw';
import {environment} from 'src/environments/environment';
import {createFixResponseHandler} from 'src/react/lib/mock-helper';

const loyaltyBaseUrl = `${environment.variablesBaseUrl}/api/loyalty`;

export const handlers = [
  rest.get(`${loyaltyBaseUrl}/transactions`, (req, res, ctx) => {
    const userId = req.url.searchParams.get('userId');
    const referenceId = req.url.searchParams.get('referenceId');
    const transactionId = req.url.searchParams.get('transactionId');
    const cardNumber = req.url.searchParams.get('cardNumber');

    if (userId) {
      return res(
        ctx.json({
          data: MOCK_LOYALTY_TRANSACTIONS.data.filter(
            (transaction) => transaction.userId === userId,
          ),
          metadata: MOCK_LOYALTY_TRANSACTIONS.metadata,
        }),
      );
    } else if (referenceId) {
      return res(
        ctx.json({
          data: MOCK_LOYALTY_TRANSACTIONS.data.filter(
            (transaction) => transaction.referenceId === referenceId,
          ),
          metadata: MOCK_LOYALTY_TRANSACTIONS.metadata,
        }),
      );
    } else if (transactionId) {
      return res(
        ctx.json({
          data: MOCK_LOYALTY_TRANSACTIONS.data.filter(
            (transaction) => transaction.id === transactionId,
          ),
          metadata: MOCK_LOYALTY_TRANSACTIONS.metadata,
        }),
      );
    } else if (cardNumber) {
      return res(
        ctx.json({
          data: MOCK_LOYALTY_TRANSACTIONS.data.filter(
            (transaction) => transaction.receiverCardNumber === cardNumber,
          ),
          metadata: MOCK_LOYALTY_TRANSACTIONS.metadata,
        }),
      );
    }

    return res(ctx.json(MOCK_LOYALTY_TRANSACTIONS));
  }),
  rest.get(`${loyaltyBaseUrl}/transactions/details/:id`, (_, res, ctx) => {
    return res(ctx.json(MOCK_LOYALTY_TRANSACTION_DETAILS));
  }),
  rest.get(`${loyaltyBaseUrl}/system/cards/:id`, (req, res, ctx) => {
    const {id} = req.params;

    if (id) {
      return res(ctx.json(MOCK_CARD_DATA.data.find((card) => card.userId === id)));
    } else {
      return res(ctx.json(MOCK_CARD_DATA));
    }
  }),
  rest.get(`${loyaltyBaseUrl}/admin/cards`, (_, res, ctx) => {
    return res(ctx.json(MOCK_CARD_DATA));
  }),
  rest.get(`${loyaltyBaseUrl}/system/dailyTransaction`, (_, res, ctx) => {
    return res(ctx.json(MOCK_DAILY_TRANSACTIONS));
  }),
  rest.get(`${loyaltyBaseUrl}/system/monthlyTransactions`, (_, res, ctx) => {
    return res(ctx.json(MOCK_MONTHLY_TRANSACTIONS));
  }),
  rest.get(`${loyaltyBaseUrl}/admin/searchLoyaltyCards`, (_, res, ctx) => {
    return res(ctx.json(MOCK_SEARCH_CARDS));
  }),
  rest.get(
    `${loyaltyBaseUrl}/admin/searchLoyaltyCards/cardActivationLimit`,
    createFixResponseHandler({
      retryCount: 0,
    }),
  ),
  rest.get(`${loyaltyBaseUrl}/admin/loyalty-members`, (req, res, ctx) => {
    const cardNumber = req.url.searchParams.get('cardNumber');

    if (cardNumber) {
      return res(
        ctx.json({
          data: MOCK_LOYALTY_MEMBERS.data.filter((member) => member.cardNumber === cardNumber),
          metadata: MOCK_LOYALTY_MEMBERS.metadata,
        }),
      );
    }

    return res(ctx.json(MOCK_LOYALTY_MEMBERS));
  }),
  rest.get(`${loyaltyBaseUrl}/admin/loyalty-members/:id`, (req, res, ctx) => {
    const {id} = req.params;

    return res(ctx.json(MOCK_LOYALTY_MEMBERS.data.find((member) => member.id === id)));
  }),
  rest.get(`${loyaltyBaseUrl}/admin/loyalty-members/by-user/:userId`, (req, res, ctx) => {
    const {userId} = req.params;

    return res(ctx.json(MOCK_LOYALTY_MEMBERS.data.find((member) => member.userId === userId)));
  }),
  rest.get(`${loyaltyBaseUrl}/admin/cards/:cardNumber`, (req, res, ctx) =>
    res(
      ctx.json({
        ...MOCK_LOYALTY_CARD_BALANCE,
        userId: req.params.cardNumber,
      }),
    ),
  ),
  rest.get(`${loyaltyBaseUrl}/system/cards/unlink/:userId`, (req, res, ctx) => {
    const transformedData = MOCK_UNLINKED_CARDS.map((card) => ({
      ...card,
      userId: req.params.userId,
    }));
    return res(ctx.json(transformedData));
  }),
];

